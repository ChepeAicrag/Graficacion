def make_pared(value, pared):
    n = 1 / 11
    for x in range(len(pared)):
        for y in range(len(pared[x])):
            v = pared[x][y]
            if v == value:
                continue
            color = (0, 0, 1)
            if v == 0:
                color = (1, 1, 1)
            if v == 3:
                color = (1, 0, 0)    
            cubo(y * n - 1, 1 - x * n, 1, color)


