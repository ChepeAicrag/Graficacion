def squad(x, y, z, n, i, c):
    r, g, b = c
    glBegin(GL_QUADS)
    glColor3f(r, g, b)
    if i == 0:
        glVertex3f(x, y, z)
        glVertex3f(x + n, y, z)
        glVertex3f(x + n, y - n, z)
        glVertex3f(x, y - n, z)
    elif i == 1:
        glVertex3f(x, y, z)
        glVertex3f(x, y, z - n)
        glVertex3f(x, y - n, z - n)
        glVertex3f(x, y - n, z)
    elif i == 2:
        glVertex3f(x, y, z)
        glVertex3f(x + n, y, z)
        glVertex3f(x + n, y, z - n)
        glVertex3f(x, y, z - n)
    glEnd()


def cubo(x, y, z, c):
    n = 1 / 11
    squad(x, y, z, n, 0, c)
    squad(x, y, z-n, n, 0, c)
    squad(x + n, y, z, n, 1, c)
    squad(x, y, z, n, 1, c)
    squad(x, y - n, z, n, 2, c)
    squad(x, y, z, n, 2, c)

