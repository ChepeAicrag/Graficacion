def move(key, x, y):
    global ry, rx, rz, px, py, pz
    move = 0.20
    key = key.decode('utf-8')
    print(key)
    if (key == 'f'):
        global window
        glutDestroyWindow(window)
        return 0

    elif (key == 'j'):
        px -= move
    elif (key == 'i'):
        pz += move
    elif (key == 'k'):
        pz -= move
    elif (key == 'l'):
        px += move

    elif (key == 'w'):
        rz += move
    elif (key == 's'):
        rz -= move
    elif (key == 'a'):
        rx -= move
    elif (key == 'd'):
        rx += move
    elif (key == 'q'):
        ry -= move
    elif (key == 'e'):
        ry += move
    glutPostRedisplay()

def flechas(key, x, y):
    move = 0.20
    global rx,px,rz,pz,ry,rz,py
    if (key == GLUT_KEY_LEFT):
        rx += move
        px += px  
    elif (key == GLUT_KEY_RIGHT):
        rx -= move
        px -= move
    elif (key == GLUT_KEY_UP):
        rz += move
        pz += move
    elif (key == GLUT_KEY_DOWN):
        rz += move
        pz += move
    glutPostRedisplay()

